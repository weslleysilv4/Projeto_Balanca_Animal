    private void calcularPesoTotal(){
        Double sum = 0.0;
        for(Bovino bov : GerBovino.getBovUnic().getBdBovino()){
            sum += bov.getPesoAnimal();
        }
        for(Equino cav : GerEquino.getGerEquinoUnic().getBdEquino()){
            sum += cav.getPesoAnimal();
        }
        lb_PesoTotal.setText("Peso Total: "+ sum);
    }
