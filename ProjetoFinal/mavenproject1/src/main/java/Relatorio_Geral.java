      public void listTabCav(){
        DefaultTableModel modelo = (DefaultTableModel)tbRelEquino.getModel();
        int posLin = 0;
        
        modelo.setRowCount(posLin);
        
        for(Equino cav:GerEquino.getGerEquinoUnic().getBdEquino()){ //for each
            modelo.insertRow(posLin, new Object[]{
                cav.getCodAnimal(),
                cav.getIdadeAnimal(),
                cav.getAlturaEquino(),
                cav.getPerimetroToracico(),
                cav.getPesoAnimal(),
                cav.getDoencaAnimal()
            });
            posLin++;
        }
    }
    public void listTabBov(){
        DefaultTableModel modelo = (DefaultTableModel)tbRelBovino.getModel();
        int posLin = 0;
        
        modelo.setRowCount(posLin);
        
        for(Bovino bov : GerBovino.getBovUnic().getBdBovino()){ //for each
            modelo.insertRow(posLin, new Object[]{
                bov.getCodAnimal(),
                bov.getIdadeAnimal(),
                bov.getPesoAnimal(),
                bov.getPesoEmArroba(),
                bov.getDoencaAnimal()
            });
            posLin++;
        }
    }
    
    public void selectTabCav(){
    
        String valLinTab = "";
        
        int posLin = tbRelEquino.getSelectedRow();
        int col = 0;
        for(col = 0; col < tbRelEquino.getColumnCount(); col++){
            valLinTab += tbRelEquino.getModel().getValueAt(posLin, col).toString();
            
            if(col+1 < tbRelEquino.getColumnCount()){
                valLinTab += " - ";
            }
        }
        
        JOptionPane.showMessageDialog(
                null,
                "Conteudo: " + valLinTab,
                "VALORES DA LINHA --> "+ (posLin + 1),
                1
        );
    }
